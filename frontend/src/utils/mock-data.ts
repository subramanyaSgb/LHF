// --- Generate mock thermal frame data ---
export function generateMockThermalFrame(width = 80, height = 60): number[][] {
  const frame: number[][] = [];
  const centerX = width / 2;
  const centerY = height / 2;
  const baseTemp = 1100 + Math.random() * 50;

  for (let y = 0; y < height; y++) {
    const row: number[] = [];
    for (let x = 0; x < width; x++) {
      const dist = Math.sqrt((x - centerX) ** 2 + (y - centerY) ** 2);
      const maxDist = Math.sqrt(centerX ** 2 + centerY ** 2);
      const normalizedDist = dist / maxDist;
      const temp = baseTemp + (1 - normalizedDist) * 250 + (Math.random() - 0.5) * 20;
      row.push(Math.round(temp));
    }
    frame.push(row);
  }
  return frame;
}
